// Abaixo está a definição do namespace. Namespaces são usados para organizar o código.
namespace IntroducaoCSharp
{
    // A classe Program contém o ponto de entrada do programa.
    class Program
    {
        // O método Main é o ponto de entrada do programa.
        // É o primeiro método que é executado quando o programa é iniciado.
        static void Main()
        {
            // Este comando imprime "Olá, Mundo!" no console.
            Console.WriteLine("Olá, Mundo!");

            // Abaixo está um exemplo de declaração de variáveis.
            int numero1 = 10; // Declara uma variável inteira chamada 'numero' e atribui o valor 10 a ela.
            string texto = "Exemplo de texto"; // Declara uma variável string chamada 'texto' e atribui um valor a ela.
            int numero2 = 20; // Declara uma variável inteira chamada 'numero2' e atribui o valor 20 a ela.


            // Este comando imprime o valor das variáveis no console.
            Console.WriteLine("Número: " + numero1);
            Console.WriteLine("Texto: " + texto);
            Console.WriteLine("Número 2: " + numero2);


        }
    }
}

  